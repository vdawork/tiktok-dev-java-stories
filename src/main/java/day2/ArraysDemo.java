package day2;

public class ArraysDemo {
		public static void main(String[] args) {
				String[] emails = new String[] {
								"email1@gmail.com",
								"email2@gmail.com",
								"email3@gmail.com"
				};
				for (int i = 0; i < emails.length; i++) {
						String email = emails[i];
						System.out.println(email);
				}
		}
}
